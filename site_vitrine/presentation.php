							<a><strong> Etape 2 : Modélisation</strong>
							 </a></br></br>
					
					</div>
					<div class="col-12 align-right">
							<a> Après avoir choisi notre outil de modélisation et suivi une courte formation aux bases de la modélisation 3D, nous nous sommes lancés dans la création de Bob-e. Après avoir appris d’innombrable raccourcis et réaliser d’innombrable tentative pour intégrer des fonctionnalités, Bob-e ressort de cette période d’effort intense avec un low design d’un écran mobile grâce à ces chenilles avec un visage pour traduire ces sentiments.

							 </a></br></br></br>
					

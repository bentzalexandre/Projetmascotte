							 </a></br></br>
					
					</div>
					<div class="col-12 align-right">
							<a> Le modèle 3D terminé, nous avons migré sur Unity pour l’animer. Nous nous sommes rapidement mis d’accord sur les différentes mises en scènes possibles pour la mascotte du fait des contraintes émises lors de la présentation. Bob-e sera alors mis en scène dans la salle de classe Beaubourg dans deux activités du quotidien à l’Institut G4 : les cours et les pauses.


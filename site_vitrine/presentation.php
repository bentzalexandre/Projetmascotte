							 </a></br></br>
					
					</div>
					<div class="col-12 align-right">
							<a> Autour d’un grand brainstorming après la présentation du projet, l’équipe a pu échanger sur la vision de la mascotte idéale que chacun imaginait. </br>
De cet échange est né l’idée de Bob-e, le robot innovant et autonome. Même si nous appréhendions la partie modélisation, Bob-e avait espoir de voir à quoi il ressemblait bien plus rapidement que prévu !</br>
Pour nous organiser, un tableau Kanban sur Trello à été mis en place afin de répartir et de suivre les tâches de toute l’équipe !

							 </a></br></br></br>
					
					</div>
